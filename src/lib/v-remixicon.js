function createSvgIcon(iconName, d, viewBox = '0 0 24 24') {
  return defineComponent({
    name: iconName,
    props: {
      size: { type: [String, Number], default: 24 },
      color: { type: String, default: 'currentColor' },
    },
    render() {
      return h(
        'svg',
        {
          width: this.size,
          height: this.size,
          viewBox,
          fill: this.color,
          xmlns: 'http://www.w3.org/2000/svg',
        },
        [h('path', { d })]
      );
    },
  });
}

const CustomIcons = Object.fromEntries(
  Object.entries(customIconPaths).map(([name, { d, viewBox }]) => [
    name,
    createSvgIcon(name, d, viewBox),
  ])
);

export default {
  install(app) {
    app.component(
      'v-remixicon',
      defineComponent({
        name: 'v-remixicon',
        props: {
          name: { type: String, required: true },
          size: { type: [String, Number], default: 24 },
          color: { type: String, default: 'currentColor' },
        },
        render() {
          const RemixIcon = RemixIcons?.[this.name];
          if (RemixIcon) {
            return h(markRaw(RemixIcon), {
              size: String(this.size),
              color: this.color,
            });
          }

          const CustomIcon = CustomIcons?.[this.name];
          if (CustomIcon) {
            return h(CustomIcon, { size: this.size, color: this.color });
          }

          console.warn(`[v-remixicon] Unknown icon: "${this.name}"`);
          return h('span', {
            style: `display:inline-block;width:${this.size}px;height:${this.size}px`,
          });
        },
      })
    );
  },
};
