export default {
  install(app) {
    app.component(
      'v-remixicon',
      defineComponent({
        name: 'v-remixicon',
        props: {
          name: { type: String, required: true },
          size: { type: [String, Number], default: 24 },
          color: { type: String, default: 'currentColor' },
        },
        render() {
          const RemixIcon = RemixIcons?.[this.name];
          if (RemixIcon) {
            return h(markRaw(RemixIcon), {
              size: String(this.size),
              color: this.color,
            });
          }

          const customKey =
            this.name.charAt(0).toUpperCase() + this.name.slice(1);
          const CustomIcon = CustomIcons?.[customKey];
          if (CustomIcon) {
            return h(CustomIcon, { size: this.size, color: this.color });
          }

          console.warn(`[v-remixicon] Unknown icon: "${this.name}"`);
          return h('span', {
            style: `display:inline-block;width:${this.size}px;height:${this.size}px`,
          });
        },
      })
    );
  },
};
